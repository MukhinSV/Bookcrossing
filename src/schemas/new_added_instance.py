from datetime import datetime

from pydantic import BaseModel, ConfigDict


class NewAddedInstance(BaseModel):
    id: int
    owner_id: int
    title: str
    author: str
    address: str
    created_at: datetime
    model_config = ConfigDict(from_attributes=True)


class NewAddedInstanceAdd(BaseModel):
    owner_id: int
    title: str
    author: str
    address: str
    created_at: datetime
